class ConstantDemo{
	public static void main(String[] Args){
		//输出字符串常量
		System.out.println("HelloWorld");
		//输出字符常量
		System.out.println('a');
		//输出整型常量
		System.out.println(10);
		//输出小数常量
		System.out.println(11.5);
		//输出布尔常量
		System.out.println(true);
		//输出空型常量
		//System.out.println(null);
	}
}